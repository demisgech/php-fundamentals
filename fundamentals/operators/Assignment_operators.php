<?php

// Augmented Assignment Operators
$x = 10;
$x += 5; // $x = $x + 5; // 15
$x -= 5; // $x = $x - 5; // 10
$x *= 5; // $x = $x * 5; // 50
$x /= 5; // $x = $x / 5; // 10
$x %= 5; // $x = $x % 5; // 0
$x **= 5; // $x = $x ** 5; // 100000
$x = $x && 19; // 19
$x = $x || 19; // 100000
$x &= 19; // $x = $x & 19; // 0
$x |= 19; // $x = $x | 19; // 19
$x ^= 19; // $x = $x ^ 19; // 19
$x <<= 19; // $x = $x << 19; // 0
$x >>= 19; // $x = $x >> 19; // 0
